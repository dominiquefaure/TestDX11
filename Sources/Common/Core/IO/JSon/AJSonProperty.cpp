#include "AJSonProperty.h"

//-------------------------------------------------------------------------------------------------
AJsonProperty::AJsonProperty()
{
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
AJsonProperty::~AJsonProperty()
{
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
JSonPropertyType AJsonProperty::GetType()const
{
	return m_type;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
const std::string& AJsonProperty::GetName()const
{
	return m_name;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
void AJsonProperty::SetName( const std::string& a_name )
{
	m_name													=	a_name;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
void AJsonProperty::Serialize( rapidjson::PrettyWriter<rapidjson::StringBuffer> & a_writer )
{
	// Add the property Key
	a_writer.Key( m_name.c_str() );

	// Add the value
	SerializeValue( a_writer );
}
