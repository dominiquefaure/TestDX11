#include "JSonBoolProperty.h"

//-------------------------------------------------------------------------------------------------
JSonBoolProperty::JSonBoolProperty()
{
	m_type													=	JSON_PROPERTY_TYPE_BOOL;
	m_value													=	false;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
JSonBoolProperty::~JSonBoolProperty()
{
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
TBool JSonBoolProperty::GetValue( )const
{
	return m_value;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
void JSonBoolProperty::SetValue( TBool a_value )
{
	m_value													=	a_value;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
void JSonBoolProperty::SerializeValue( rapidjson::PrettyWriter<rapidjson::StringBuffer> & a_writer )
{
	a_writer.Bool( m_value );
}
//-------------------------------------------------------------------------------------------------
