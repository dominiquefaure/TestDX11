#include "JSonFloatProperty.h"


//-------------------------------------------------------------------------------------------------
JSonFloatProperty::JSonFloatProperty()
{
	m_type													=	JSON_PROPERTY_TYPE_FLOAT;
	m_value													=	0;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
JSonFloatProperty::~JSonFloatProperty()
{
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
TFloat64 JSonFloatProperty::GetValue( )const
{
	return m_value;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
void JSonFloatProperty::SetValue( TFloat64 a_value )
{
	m_value													=	a_value;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
void JSonFloatProperty::SerializeValue( rapidjson::PrettyWriter<rapidjson::StringBuffer> & a_writer )
{
	a_writer.Double( m_value );
}
//-------------------------------------------------------------------------------------------------
