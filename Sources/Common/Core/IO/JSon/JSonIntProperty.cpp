#include "JSonIntProperty.h"


//-------------------------------------------------------------------------------------------------
JSonIntProperty::JSonIntProperty()
{
	m_type													=	JSON_PROPERTY_TYPE_INT;
	m_value													=	0;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
JSonIntProperty::~JSonIntProperty()
{
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
TUint64 JSonIntProperty::GetValue( )const
{
	return m_value;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
void JSonIntProperty::SetValue( TUint64 a_value )
{
	m_value													=	a_value;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
void JSonIntProperty::SerializeValue( rapidjson::PrettyWriter<rapidjson::StringBuffer> & a_writer )
{
	a_writer.Int64( m_value );
}
//-------------------------------------------------------------------------------------------------
