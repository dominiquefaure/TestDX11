#include "JSonStringProperty.h"


//-------------------------------------------------------------------------------------------------
JSonStringProperty::JSonStringProperty()
{
	m_type													=	JSON_PROPERTY_TYPE_STRING;
	m_value													=	"";
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
JSonStringProperty::~JSonStringProperty()
{
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
const std::string& JSonStringProperty::GetValue( )const
{
	return m_value;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
void JSonStringProperty::SetValue( const std::string& a_value )
{
	m_value													=	a_value;
}
//-------------------------------------------------------------------------------------------------

//-------------------------------------------------------------------------------------------------
void JSonStringProperty::SerializeValue( rapidjson::PrettyWriter<rapidjson::StringBuffer> & a_writer )
{
	a_writer.String( m_value.c_str() );
}
//-------------------------------------------------------------------------------------------------
