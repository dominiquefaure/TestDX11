#include "RhiManager.h"

#ifdef _DEBUG
#include "RhiManager.inl"
#endif

//---------------------------------------------------------------------------------------------
RhiManager::RhiManager()
{
	m_device												=	NULL;
}
//---------------------------------------------------------------------------------------------

//---------------------------------------------------------------------------------------------
RhiManager::~RhiManager()
{
	SAFE_DELETE( m_device );
}
//---------------------------------------------------------------------------------------------

//---------------------------------------------------------------------------------------------
void RhiManager::Init( HWND a_handle , TUint32 a_width , TUint32 a_height )
{
	m_device												=	new GraphicDeviceDX11();
	m_device->Init( a_handle , a_width , a_height );
}
//---------------------------------------------------------------------------------------------

//---------------------------------------------------------------------------------------------
void RhiManager::BeginFrame()
{
	m_device->BeginFrame();
}
//---------------------------------------------------------------------------------------------

//---------------------------------------------------------------------------------------------
void RhiManager::EndFrame()
{
	m_device->EndFrame();
}
//---------------------------------------------------------------------------------------------

