#include "InputManager.h"

#include "KeyboardDevice.h"

#include "../Core/CoreMacros.h"


//-----------------------------------------------------------------------------
InputManager::InputManager()
{
	m_keyboard												=	NULL;
}
//-----------------------------------------------------------------------------

//-----------------------------------------------------------------------------
InputManager::~InputManager()
{
	SAFE_DELETE( m_keyboard );
}
//-----------------------------------------------------------------------------

//-----------------------------------------------------------------------------
KeyboardDevice* InputManager::GetKeyboard()
{
	return m_keyboard;
}
//-----------------------------------------------------------------------------

//-----------------------------------------------------------------------------
void InputManager::OnInit()
{
	m_keyboard												=	new KeyboardDevice();
	m_keyboard->Init();
}
//-----------------------------------------------------------------------------

//-----------------------------------------------------------------------------
void InputManager::Update()
{
	m_keyboard->Update();
}
//-----------------------------------------------------------------------------
