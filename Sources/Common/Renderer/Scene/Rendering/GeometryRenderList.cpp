#include "GeometryRenderList.h"


//------------------------------------------------------------------------------------
GeometryRenderList::GeometryRenderList()
{
}
//------------------------------------------------------------------------------------

//------------------------------------------------------------------------------------
GeometryRenderList::~GeometryRenderList()
{
	Clear();
}
//------------------------------------------------------------------------------------

//------------------------------------------------------------------------------------
void GeometryRenderList::AddElement( GeometryRenderElement* a_element )
{
	//add the element to the List
	m_elements.push_back( a_element );
}
//------------------------------------------------------------------------------------

//------------------------------------------------------------------------------------
void GeometryRenderList::Render(  RhiGraphicContext* a_context )
{
	TUint32 t_count											=	m_elements.size();
	for( int i = 0 ; i < t_count ; i ++ )
	{
		m_elements[ i ]->Render( a_context );
	}
}
//------------------------------------------------------------------------------------

//------------------------------------------------------------------------------------
void GeometryRenderList::Clear( )
{
	SAFE_DELETE_OBJECT_VECTOR_ARRAY( m_elements );
}
//------------------------------------------------------------------------------------
